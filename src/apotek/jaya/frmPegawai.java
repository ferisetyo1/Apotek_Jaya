    private void showData() {
        refreshData(model, jPegawai);
        model = (DefaultTableModel) jPegawai.getModel();
        jPegawai.setModel(model);
        try {
            ResultSet rs = preState("select * from pegawai").executeQuery();
            while (rs.next()) {
                model.addRow(
                        new Object[]{
                            rs.getString(1),
                            rs.getString(2),
                            rs.getString(3),
                            rs.getString(4),
                            rs.getString(5),
                            rs.getString(6)
                        }
                );
            }
            lJumlah.setText("Jumlah Data : " + model.getRowCount());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void insertData() {
        try {
            PreparedStatement pst = con.prepareStatement("insert into pegawai values(?,?,?,?,?,?)");
            pst.setString(1, eID.getText());
            pst.setString(2, eNama.getText());
            pst.setString(3, eAlamat.getText());
            pst.setString(4, eNoTelp.getText());
            String theDate = dt.format(dtLahir.getDate());
            pst.setString(5, theDate);
            pst.setString(6, String.valueOf(cmBagian.getSelectedItem()));
            pst.executeUpdate();
            showData();
            JOptionPane.showMessageDialog(null, "Tambah Data Berhasil!");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void updateData() {
        try {
            PreparedStatement pst = con.prepareStatement("update pegawai set ID_Pegawai=?,Nama=?,Alamat=?,No_Telp=?,Tgl_Lahir=?,Bagian=? where ID_Pegawai='" + ID + "'");
            pst.setString(1, eID.getText());
            pst.setString(2, eNama.getText());
            pst.setString(3, eAlamat.getText());
            pst.setString(4, eNoTelp.getText());
            String theDate = dt.format(dtLahir.getDate());
            pst.setString(5, theDate);
            pst.setString(6, String.valueOf(cmBagian.getSelectedItem()));
            pst.executeUpdate();
            showData();
            JOptionPane.showMessageDialog(null, "Ubah Data Berhasil!");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void deleteData() {
        try {
            PreparedStatement pst = con.prepareStatement("delete from pegawai where ID_Pegawai='" + ID + "'");
            pst.executeUpdate();
            showData();
            JOptionPane.showMessageDialog(null, "Hapus Data Berhasil!");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

