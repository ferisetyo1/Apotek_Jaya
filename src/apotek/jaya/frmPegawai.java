    private void btnSimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSimpanMouseClicked
        if (eID.getText().isEmpty() && eNama.getText().isEmpty() && eAlamat.getText().isEmpty() && eNoTelp.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Isi Seluruh Data!");
        } else {
            int dialogResult = JOptionPane.showConfirmDialog(null, "Apakah Kamu Yakin?", "Peringatan", 0);
            if (dialogResult == JOptionPane.YES_OPTION) {
                if (tambah == true) {
                    insertData();
                } else {
                    updateData();
                }
                buatEnabled(false);
                btnTambah.setEnabled(true);
                btnUbah.setEnabled(true);
                btnHapus.setEnabled(true);
                eID.setText("");
                eNama.setText("");
                eAlamat.setText("");
                eNoTelp.setText("");
            }
        }
    }//GEN-LAST:event_btnSimpanMouseClicked

