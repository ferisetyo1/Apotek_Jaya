    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String pass = new String(ePass.getPassword());
        String ret = new String(eRetype.getPassword());
        if (eID.getText().isEmpty() && eUser.getText().isEmpty() && pass.isEmpty() && ret.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Isi Seluruh Data!");
        } else {
            if (pass.equals(ret)) {
                int dialogResult = JOptionPane.showConfirmDialog(null, "Apakah Kamu Yakin?", "Peringatan", 0);
                if (dialogResult == JOptionPane.YES_OPTION) {
                    if (tambah == true) {
                        insertData();
                    } else {
                        updateData();
                    }
                    buatEnabled(false);
                    btnTambah.setEnabled(true);
                    btnUbah.setEnabled(true);
                    btnHapus.setEnabled(true);
                    eID.setText("");
                    eUser.setText("");
                    ePass.setText("");
                    eRetype.setText("");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Password Tidak Sama!");
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnBatalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBatalActionPerformed
        String pass = new String(ePass.getPassword());
        String ret = new String(eRetype.getPassword());
        if (eID.getText().isEmpty() && eUser.getText().isEmpty() && pass.isEmpty() && ret.isEmpty()) {
            buatEnabled(false);
            btnTambah.setEnabled(true);
            btnUbah.setEnabled(true);
            btnHapus.setEnabled(true);
        } else {
            eID.setText("");
            eUser.setText("");
            ePass.setText("");
            eRetype.setText("");
        }
    }//GEN-LAST:event_btnBatalActionPerformed

