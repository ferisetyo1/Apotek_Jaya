    private void jSupplierMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jSupplierMouseClicked
        int row = jSupplier.getSelectedRow();
        String hasil = (jSupplier.getModel().getValueAt(row, 0).toString());
        ID = hasil;
        eID.setText((jSupplier.getModel().getValueAt(row, 0).toString()));
        eUser.setText((jSupplier.getModel().getValueAt(row, 1).toString()));
        ePass.setText((jSupplier.getModel().getValueAt(row, 5).toString()));
    }//GEN-LAST:event_jSupplierMouseClicked

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        if (eID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Pilih Data Dahulu!");
        } else {
            ubah = true;
            tambah = false;
            btnTambah.setEnabled(false);
            btnHapus.setEnabled(false);
            buatEnabled(true);
            eID.grabFocus();
            ePass.setEnabled(false);
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        if (eID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Pilih Data Dahulu!");
        } else {
            int dialogResult = JOptionPane.showConfirmDialog(null, "Apakah Kamu Yakin?", "Peringatan", 0);
            if (dialogResult == JOptionPane.YES_OPTION) {
                deleteData();
            }
        }
        eID.setText("");
        eUser.setText("");
        ePass.setText("");
        eRetype.setText("");
    }//GEN-LAST:event_btnHapusActionPerformed

