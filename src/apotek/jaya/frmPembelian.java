    private void showObat() {
        refreshData(model, jJual);
        model = (DefaultTableModel) jObat.getModel();
        jObat.setModel(model);
        try {
            ResultSet rs = preState("select * from obat").executeQuery();
            while (rs.next()) {
                model.addRow(
                        new Object[]{
                            rs.getString(1),
                            rs.getString(2),
                            rs.getString(3),
                            rs.getInt(4),
                            rs.getInt(5),
                            rs.getDate(6)
                        }
                );
            }
            lJumlah.setText("Jumlah Data : " + model.getRowCount());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void insertFaktur() {
        try {
            PreparedStatement pst = con.prepareStatement("insert into faktur values(?,?,?,?)");
            pst.setString(1, eFaktur.getText());
            pst.setString(2, eID.getText());
            pst.setInt(3, Integer.valueOf(eTotal.getText()));
            pst.setInt(4, Integer.valueOf(eHarga.getText()) * Integer.valueOf(eTotal.getText()));
            pst.executeUpdate();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void insertData() {
        try {
            PreparedStatement pst = con.prepareStatement("insert into penjualan values(?,?,?,?)");
            pst.setString(1, eFaktur.getText());
            Pegawai peg = new Pegawai();

            pst.setString(2, Pegawai.getID_Peg());
            pst.setString(3, getTgl());
            pst.setInt(4, Integer.valueOf(eHarga.getText()) * Integer.valueOf(eTotal.getText()));
            pst.executeUpdate();
            insertFaktur();
            JOptionPane.showMessageDialog(null, "Tambah Data Berhasil!");
            buatEnabled(false);
            btnJual.setEnabled(true);
            btnHapus.setEnabled(true);
            eID.setText("");
            eNama.setText("");
            eIDSUpp.setText("");
            eHarga.setText("");
            eIDSUpp.setText("");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void updateData() {
        try {
            PreparedStatement pst = con.prepareStatement("update obat set ID_Obat=?,Nama=?,Kategori=?,Harga=?,Stok=?,Tgl_Kedaluarsa=? where ID_Obat='" + ID + "'");
            pst.setString(1, eID.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Ubah Data Berhasil!");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void deleteData() {
        try {
            PreparedStatement pst = con.prepareStatement("delete from obat where ID_Obat='" + ID + "'");
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Hapus Data Berhasil!");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

