    private void btnTambahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnTambahMouseClicked
        if (eID.getText().isEmpty() && eIDSUpp.getText().isEmpty() && eHarga.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Isi Seluruh Data!");
        } else {
            int dialogResult = JOptionPane.showConfirmDialog(null, "Apakah Kamu Yakin?", "Peringatan", 0);
            if (dialogResult == JOptionPane.YES_OPTION) {
                if (tambah == true) {
                    insertData();
                } else {
                    updateData();
                }
                buatEnabled(false);
                btnJual.setEnabled(true);
                btnHapus.setEnabled(true);
                eID.setText("");
                eIDSUpp.setText("");
                eHarga.setText("");
            }
        }
    }//GEN-LAST:event_btnTambahMouseClicked

