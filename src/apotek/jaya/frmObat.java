    private void jObatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jObatMouseClicked
        int row = jObat.getSelectedRow();
        String hasil = (jObat.getModel().getValueAt(row, 0).toString());
        ID = hasil;
        eID.setText((jObat.getModel().getValueAt(row, 0).toString()));
        eNama.setText((jObat.getModel().getValueAt(row, 1).toString()));
        cmKategori.setSelectedItem((jObat.getModel().getValueAt(row, 2).toString()));
        eHarga.setText((jObat.getModel().getValueAt(row, 3).toString()));
        eStok.setText((jObat.getModel().getValueAt(row, 4).toString()));
        dtKedal.setSelectedItem((jObat.getModel().getValueAt(row, 5).toString()));
    }//GEN-LAST:event_jObatMouseClicked

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if (eID.getText().isEmpty() && eNama.getText().isEmpty() && eHarga.getText().isEmpty() && eStok.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Isi Seluruh Data!");
        } else {
            int dialogResult = JOptionPane.showConfirmDialog(null, "Apakah Kamu Yakin?", "Peringatan", 0);
            if (dialogResult == JOptionPane.YES_OPTION) {
                if (tambah == true) {
                    insertData();
                } else {
                    updateData();
                }
                buatEnabled(false);
                btnTambah.setEnabled(true);
                btnUbah.setEnabled(true);
                btnHapus.setEnabled(true);
                eID.setText("");
                eNama.setText("");
                eHarga.setText("");
                eStok.setText("");
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        tambah = true;
        ubah = false;
        btnUbah.setEnabled(false);
        btnHapus.setEnabled(false);
        buatEnabled(true);
        eID.grabFocus();
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        if (eID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Pilih Data Dahulu!");
        } else {
            ubah = true;
            tambah = false;
            btnTambah.setEnabled(false);
            btnHapus.setEnabled(false);
            buatEnabled(true);
            eID.grabFocus();
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnBatalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBatalActionPerformed
        if (eID.getText().isEmpty() && eNama.getText().isEmpty() && eHarga.getText().isEmpty() && eStok.getText().isEmpty()) {
            buatEnabled(false);
            btnTambah.setEnabled(true);
            btnUbah.setEnabled(true);
            btnHapus.setEnabled(true);
        } else {
            eID.setText("");
            eNama.setText("");
            eHarga.setText("");
            eStok.setText("");
        }
    }//GEN-LAST:event_btnBatalActionPerformed

