    private void showData() {
        refreshData(model, jObat);
        model = (DefaultTableModel) jObat.getModel();
        jObat.setModel(model);
        try {
            ResultSet rs = preState("select * from obat").executeQuery();
            while (rs.next()) {
                model.addRow(
                        new Object[]{
                            rs.getString(1),
                            rs.getString(2),
                            rs.getString(3),
                            rs.getInt(4),
                            rs.getInt(5),
                            rs.getDate(6)
                        }
                );
            }
            lJumlah.setText("Jumlah Data : " + model.getRowCount());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void insertData() {
        try {
            PreparedStatement pst = con.prepareStatement("insert into obat values(?,?,?,?,?,?)");
            pst.setString(1, eID.getText());
            pst.setString(2, eNama.getText());
            pst.setString(3, String.valueOf(cmKategori.getSelectedItem()));
            pst.setString(4, eHarga.getText());
            pst.setString(5, eStok.getText());
            String theDate = dt.format(dtKedal.getDate());
            pst.setString(6, theDate);
            pst.executeUpdate();
            showData();
            JOptionPane.showMessageDialog(null, "Tambah Data Berhasil!");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void updateData() {
        try {
            PreparedStatement pst = con.prepareStatement("update obat set ID_Obat=?,Nama=?,Kategori=?,Harga=?,Stok=?,Tgl_Kedaluarsa=? where ID_Obat='" + ID + "'");
            pst.setString(1, eID.getText());
            pst.setString(2, eNama.getText());
            pst.setString(3, String.valueOf(cmKategori.getSelectedItem()));
            pst.setString(4, eHarga.getText());
            pst.setString(5, eStok.getText());
            String theDate = dt.format(dtKedal.getDate());
            pst.setString(6, theDate);
            pst.executeUpdate();
            showData();
            JOptionPane.showMessageDialog(null, "Ubah Data Berhasil!");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void deleteData() {
        try {
            PreparedStatement pst = con.prepareStatement("delete from obat where ID_Obat='" + ID + "'");
            pst.executeUpdate();
            showData();
            JOptionPane.showMessageDialog(null, "Hapus Data Berhasil!");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void buatEnabled(Boolean z) {
        cmKategori.setSelectedIndex(0);
        eID.setEnabled(z);
        eNama.setEnabled(z);
        cmKategori.setEnabled(z);
        eHarga.setEnabled(z);
        eStok.setEnabled(z);
        btnSimpan.setEnabled(z);
        btnBatal.setEnabled(z);
        dtKedal.setEnabled(z);
    }
